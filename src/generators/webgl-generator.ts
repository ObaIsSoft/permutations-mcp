import { DesignGenome } from "../genome/types.js";

/**
 * WebGL / React Three Fiber Component Generator
 *
 * All 4 materials are fully implemented. Geometry is driven by ch15_biomarker:
 *   organic   → torusKnotGeometry (flowing, irregular)
 *   fractal   → icosahedronGeometry (geodesic, self-similar)
 *   monolithic → boxGeometry / octahedronGeometry (rigid, architectural)
 *
 * Roughness, transmission, and intensity values come directly from the genome
 * — no hardcoded aesthetic decisions.
 */
export class WebGLGenerator {
  generateR3F(genome: DesignGenome): string {
    const { material, roughness, transmission } = genome.chromosomes.ch14_physics;
    const { geometry, complexity } = genome.chromosomes.ch15_biomarker;
    const { intensity } = genome.chromosomes.ch13_atmosphere;
    const { animate } = genome.chromosomes.ch18_rendering;
    const { hue, saturation, lightness } = genome.chromosomes.ch5_color_primary;
    const entropy = genome.chromosomes.ch12_signature?.entropy ?? 0.5;

    // Derive geometry args from biomarker + entropy
    const geometryJSX = this.buildGeometry(geometry, complexity, entropy);
    const colorVal = `hsl(${hue}, ${Math.round(saturation * 100)}%, ${Math.round(lightness * 100)}%)`;
    const animateCode = animate
      ? `useFrame((_, delta) => { if (meshRef.current) { meshRef.current.rotation.x += delta * ${(0.05 + complexity * 0.2).toFixed(3)}; meshRef.current.rotation.y += delta * ${(0.08 + complexity * 0.3).toFixed(3)}; } });`
      : "// Animation disabled by genome (ch18_rendering.animate = false)";

    const materialJSX = this.buildMaterial(material, roughness, transmission, intensity, colorVal);

    return `// React Three Fiber — Generated by Permutations MCP
// Material: ${material} | Geometry: ${geometry} | DNA: ${genome.dnaHash.slice(0, 8)}
import { useRef } from 'react';
import { Canvas, useFrame } from '@react-three/fiber';
import { Environment${material === "glass" ? ", MeshTransmissionMaterial" : ""} } from '@react-three/drei';
import type { Mesh } from 'three';

function ProceduralOrganism() {
  const meshRef = useRef<Mesh>(null);

  ${animateCode}

  return (
    <>
      {/* Background glow */}
      <mesh scale={2} position={[0, 0, -2]}>
        <sphereGeometry args={[1.5, 32, 32]} />
        <meshBasicMaterial color="${colorVal}" transparent opacity={${(0.05 + intensity * 0.1).toFixed(2)}} />
      </mesh>

      {/* Main organism */}
      <mesh ref={meshRef} scale={1.2}>
        ${geometryJSX}
        ${materialJSX}
      </mesh>
    </>
  );
}

export function Procedural3D() {
  return (
    <Canvas camera={{ position: [0, 0, 6], fov: 50 }}>
      <ambientLight intensity={${(0.3 + intensity * 0.5).toFixed(2)}} />
      <pointLight position={[5, 5, 5]} intensity={${(1.0 + intensity).toFixed(2)}} color="${colorVal}" />
      <Environment preset="${this.selectEnvironment(material)}" />
      <ProceduralOrganism />
    </Canvas>
  );
}`;
  }

  private buildGeometry(geometry: "organic" | "fractal" | "monolithic", complexity: number, entropy: number = 0.5): string {
    if (geometry === "organic") {
      // Select shape based on entropy × complexity so same keyword → different geometry per DNA
      // 6 distinct organic shapes — torusKnot is just one of them
      const shapeSelector = (entropy * 0.6 + complexity * 0.4);

      if (shapeSelector < 0.15) {
        // Simple torus — minimal, ring-like (low entropy, low complexity)
        const r = (0.3 + complexity * 0.4).toFixed(2);
        const tube = (0.08 + complexity * 0.12).toFixed(2);
        return `<torusGeometry args={[${r}, ${tube}, 64, 120]} />`;

      } else if (shapeSelector < 0.30) {
        // Sphere with high segment count — cellular, membrane-like
        const segments = Math.round(32 + complexity * 64);
        return `<sphereGeometry args={[1.1, ${segments}, ${segments}} />`;

      } else if (shapeSelector < 0.50) {
        // Classic torusKnot with varied p/q from complexity
        const p = Math.max(2, Math.round(2 + complexity * 5));
        const q = Math.max(3, Math.round(3 + complexity * 4));
        return `<torusKnotGeometry args={[1, ${(0.25 + complexity * 0.2).toFixed(2)}, 128, 32, ${p}, ${q}]} />`;

      } else if (shapeSelector < 0.65) {
        // Extreme torusKnot — high p/q for chaotic biology (high entropy)
        const p = Math.max(3, Math.round(3 + entropy * 9));
        const q = Math.max(5, Math.round(5 + entropy * 7));
        return `<torusKnotGeometry args={[0.9, ${(0.15 + entropy * 0.15).toFixed(2)}, 256, 32, ${p}, ${q}]} />`;

      } else if (shapeSelector < 0.80) {
        // Icosahedron (geodesic sphere) — biological cell wall
        const detail = Math.min(5, Math.round(entropy * 5));
        return `<icosahedronGeometry args={[1.15, ${detail}]} />`;

      } else {
        // Dodecahedron — 12-face platonic solid, pentagonal biology
        return `<dodecahedronGeometry args={[1.1, ${Math.round(entropy * 2)}]} />`;
      }

    } else if (geometry === "fractal") {
      // Icosahedron — geodesic, self-similar. Detail level from complexity.
      const detail = Math.min(4, Math.floor(complexity * 5));
      return `<icosahedronGeometry args={[1.2, ${detail}]} />`;
    } else {
      // Monolithic — octahedron for medium complexity, box for low
      if (complexity > 0.5) {
        return `<octahedronGeometry args={[1.2, ${Math.floor(complexity * 3)}]} />`;
      }
      return `<boxGeometry args={[${(1.5 + complexity).toFixed(1)}, ${(1.5 + complexity).toFixed(1)}, ${(1.5 + complexity).toFixed(1)}]} />`;
    }
  }

  private buildMaterial(
    material: "glass" | "metallic" | "neumorphism" | "matte",
    roughness: number,
    transmission: number,
    intensity: number,
    color: string
  ): string {
    if (material === "glass") {
      return `<MeshTransmissionMaterial
          roughness={${roughness.toFixed(3)}}
          transmission={${Math.max(0.7, transmission).toFixed(3)}}
          thickness={${(0.5 + intensity).toFixed(2)}}
          ior={${(1.4 + intensity * 0.4).toFixed(2)}}
          color="${color}"
          emissive="${color}"
          emissiveIntensity={${(intensity * 0.3).toFixed(2)}}
        />`;
    } else if (material === "metallic") {
      return `<meshStandardMaterial
          metalness={1}
          roughness={${roughness.toFixed(3)}}
          color="${color}"
          envMapIntensity={${(0.5 + intensity * 1.5).toFixed(2)}}
        />`;
    } else if (material === "neumorphism") {
      // Soft, light-extruded — phong with specular glow
      return `<meshPhongMaterial
          color="${color}"
          specular="${color}"
          shininess={${Math.round(30 + intensity * 70)}}
          emissive="${color}"
          emissiveIntensity={${(intensity * 0.15).toFixed(2)}}
        />`;
    } else {
      // Matte — no reflections, flat lambert
      return `<meshLambertMaterial
          color="${color}"
          emissive="${color}"
          emissiveIntensity={${(intensity * 0.05).toFixed(2)}}
        />`;
    }
  }

  private selectEnvironment(material: "glass" | "metallic" | "neumorphism" | "matte"): string {
    switch (material) {
      case "glass": return "city";
      case "metallic": return "studio";
      case "neumorphism": return "sunset";
      case "matte": return "apartment";
    }
  }
}
